package ArraysDemo;

public class SimpleCode {

	public static void main(String[] args) {
		
		
		int[] marks=new int[5];
		//arrayname[0]=value;
		marks[0]=10;
		marks[1]=20;
		marks[2]=30;
		marks[3]=40;
		marks[4]=50;

		//n-1, n= arraysize
		
		for(int i:marks)
		{
		System.out.println(i);
		}
		
	
	}

}
